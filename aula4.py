# Tipos int e float
# int -> Número inteiro
# Tipo int representa qualquer número
# Positivo ou negativo. int sem sinal é considerado positivo
print(11) #int
print(-11) #int
print(0) # int

# float -> número com ponto flutuante
# o tipo representa qualquer número positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1,5)

# função type mostra o tipo que o Python inferiu ao valor
print(type('Gabi'))
print(type(45))
print(type(1.1), type(0.0))
