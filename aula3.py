# Tipos de Dados
# Python -> linguagem de programação
# str → string → texto
# Tipagem -> dinâmica / forte
# Strings são textos dentro de aspas
# Aspas simples ou duplas
print(1234) # número
print('Gabi', 'Martinez', sep='-') # texto
print(1, 'Gabi"')
print("Gabi") # texto
# Escape
print("Gabi \"Martinez") # O Escape (\) ignora o próximo caractere
# r
print(r"Gabi \"Martinez") # r mostra os caracteres escondidos (escape)
